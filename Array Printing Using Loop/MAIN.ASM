DOSSEG
.MODEL SMALL  ;MODEL DIRECTIVE
.STACK 100H   ;STACK DIRECTIVE
.DATA         ;VARIABLE DECLERATION
ARRAY DB'A','B','C'
.CODE         ;CODE SPACE                                     
MAIN PROC

MOV AX,@DATA
MOV DS,AX
MOV SI,OFFSET ARRAY
MOV CX,3

L1:
MOV DX,[SI]
MOV AH,2
INT 21H
INC SI
LOOP L1

MOV AH,4CH    ;TO EXIT THE REGISTER
INT 21H

MAIN ENDP
END MAIN

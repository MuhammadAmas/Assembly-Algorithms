DOSSEG
.MODEL SMALL  ;MODEL DIRECTIVE
.STACK 100H   ;STACK DIRECTIVE
.DATA         ;VARIABLE DECLERATION
VAR1 DB 100 DUP('$')
.CODE         ;CODE SPACE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
MOV SI,OFFSET VAR1
L1:
MOV AH,1
INT 21H
CMP AL,13
JE PROGRAMMED

MOV [SI],AL
INC SI
JMP L1

PROGRAMMED:
MOV DX,OFFSET VAR1
MOV AH,9
INT 21H

MOV AH,4CH    ;TO EXIT THE REGISTER
INT 21H

MAIN ENDP
END MAIN
